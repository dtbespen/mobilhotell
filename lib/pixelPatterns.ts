/**
 * Pixel Art Pattern System
 *
 * Each pattern is a 16x16 grid where numbers map to color roles:
 * 0 = transparent
 * 1 = skin color
 * 2 = skin shadow (darker skin)
 * 3 = primary color (class-based)
 * 4 = secondary color (class-based, darker)
 * 5 = accent color (class-based, lighter)
 * 6 = hair color
 * 7 = hair shadow (darker hair)
 * 8 = eye color (dark)
 * 9 = eye highlight (white)
 * A = boot/shoe color
 * B = metallic/buckle highlight
 */

